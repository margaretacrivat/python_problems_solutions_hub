# Task:  Write a program to find how many times substring “Alex” appears in the given string.
# Expected output: "Alex appeared 2 times."

# Hint: Return the count of a given substring from a string.

# Solution:
str_x = "Alex is a good developer. Alex is a writer."
name_x = str_x.count("Alex")
print(f"Alex appeared {name_x} times.")
